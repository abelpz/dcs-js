/**
 * AnnotatedTagObject contains meta information of the tag object
 * @export
 * @interface AnnotatedTagObject
 */

export interface AnnotatedTagObject {
  /**
   *
   * @type {string}
   * @memberof AnnotatedTagObject
   */
  sha: string;
  /**
   *
   * @type {string}
   * @memberof AnnotatedTagObject
   */
  type: string;
  /**
   *
   * @type {string}
   * @memberof AnnotatedTagObject
   */
  url: string;
}
