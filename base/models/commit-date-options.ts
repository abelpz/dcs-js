/**
 * CommitDateOptions store dates for GIT_AUTHOR_DATE and GIT_COMMITTER_DATE
 * @export
 * @interface CommitDateOptions
 */

export interface CommitDateOptions {
  /**
   *
   * @type {string}
   * @memberof CommitDateOptions
   */
  author: string;
  /**
   *
   * @type {string}
   * @memberof CommitDateOptions
   */
  committer: string;
}
