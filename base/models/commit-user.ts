/**
 * CommitUser contains information of a user in the context of a commit.
 * @export
 * @interface CommitUser
 */

export interface CommitUser {
  /**
   *
   * @type {string}
   * @memberof CommitUser
   */
  date: string;
  /**
   *
   * @type {string}
   * @memberof CommitUser
   */
  email: string;
  /**
   *
   * @type {string}
   * @memberof CommitUser
   */
  name: string;
}
